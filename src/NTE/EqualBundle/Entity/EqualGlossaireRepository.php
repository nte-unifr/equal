namespace NTE\EqualBundle\Entity;

use Doctrine\ORM\EntityRepository;
use Symfony\Component\HttpKernel\Exception\NotFoundHttpException;

/**
 * EqualGlossaireRepository
 *
 * This class was generated by the Doctrine ORM. Add your own custom
 * repository methods below.
 */
class EqualGlossaireRepository extends EntityRepository
{
	public function getGlossaire($langue)
	{
		$glossaire = $this->findBy(
			array('langue' => $langue), 
			array('title' => 'ASC'))
		;

		if (!$glossaire) {
			throw new NotFoundHttpException('Le glossaire est vide');
		}

    	return $glossaire;

	}

	public function getGlossaireAlpha($langue)
	{
		$glossaire = $this->findBy(
			array('langue' => $langue), 
			array('title' => 'ASC'))
		;

		if (!$glossaire) {
			throw new NotFoundHttpException('Le glossaire est vide');
		}

		$alpha = array();

		foreach($glossaire as $glo) {
			$letter = substr($glo->getTitle(), 0, 1);
			if (!in_array($letter, $alpha)) {
				$alpha[] = $letter;
			}
		}
		
    	return $alpha;

	}
}
