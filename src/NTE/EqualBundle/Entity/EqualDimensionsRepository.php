namespace NTE\EqualBundle\Entity;

use Doctrine\ORM\EntityRepository;

/**
 * EqualDimensionsRepository
 *
 * This class was generated by the Doctrine ORM. Add your own custom
 * repository methods below.
 */
class EqualDimensionsRepository extends EntityRepository
{
	public function getDimCheckbox($dimension_id)
	{
		$qb = $this->createQueryBuilder('d')
					->leftJoin('d.items', 'i', 'WITH', 'i.dimension = :dimension_id')
					->addSelect('i')
					->setParameter('dimension_id', $dimension_id)
		;

		return $qb->getQuery()->getResult();
	}

	public function getItemsFromDim($dimension_id)
	{
		$qb = $this->createQueryBuilder('i')
					->leftJoin('i.dimension', 'd', 'WITH', 'd.id = :dimension_id')
					->setParameter('dimension_id', $dimension_id)
		;

		return $qb->getQuery()->getResult();
	}

}
